#include "../inc/libft.h"

int	ft_str_case_cmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (-1);
	while (s1[i] && ft_tolower(s1[i]) == ft_tolower(s2[i]))
		i++;
	return (s1[i] - s2[i]);
}
